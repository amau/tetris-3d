var columns = 10;
var rows = 22;


function draw()
{
	
	for ( var y = 2; y < rows ; y++ )
	{
		var newRowContent = "<tr><td>"+drawRow(board[y])+"</td></tr>";
		console.log(newRowContent);
		$("#tetris tbody").append(newRowContent);
	}
}

function drawRow(row)
{
	var out = "";
	for ( var x = 0; x < columns ; x++ )
	{
		out = out + (row[x]?"1":"0");
	}
	return out;
}

function tetris()
{
	draw();
}


$(document).ready(function()
{
	tetris();
});
